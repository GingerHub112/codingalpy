import math

x = 23.56
y = 234

print("Floor and Ceilling of 23.56 is", str(math.floor(x)), str(math.ceil(x)))
print("x copy sign y is", math.copysign(x, y))
print("SQRT of 23.56 is", math.sqrt(x))
print("and GCD of y is", int(math.gcd(int(x))))
print("Cosine and sinus demo", math.cos(x), math.sin(y))